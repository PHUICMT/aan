// Shared test fixtures: isolated data root + DB seeded via the real migrations.

use crate::db;
use rusqlite::{params, Connection};
use std::path::{Path, PathBuf};
use tempfile::TempDir;

pub(crate) fn temp_data_root() -> (TempDir, PathBuf) {
    let dir = tempfile::tempdir().expect("create tempdir");
    let root = dir.path().to_path_buf();
    (dir, root)
}

pub(crate) fn fresh_db(root: &Path) -> Connection {
    db::open(root).expect("open db")
}

#[allow(clippy::too_many_arguments)]
pub(crate) fn insert_test_series(
    conn: &Connection,
    pid: i64,
    name: &str,
    kind: &str,
    local_chapter_count: i64,
    is_favorite: i64,
    reading_status: Option<&str>,
    last_updated: Option<&str>,
    added_at: Option<&str>,
) {
    conn.execute(
        "INSERT INTO series (pid, name, type, status, chapter_count, local_chapter_count,
                              is_favorite, reading_status, last_updated, added_at)
         VALUES (?1, ?2, ?3, 0, ?4, ?4, ?5, ?6, ?7, ?8)",
        params![
            pid,
            name,
            kind,
            local_chapter_count,
            is_favorite,
            reading_status,
            last_updated,
            added_at
        ],
    )
    .expect("insert series");
}

pub(crate) fn insert_test_chapter(
    conn: &Connection,
    chapter_id: &str,
    pid: i64,
    chapter_no: f64,
    title: &str,
    is_downloaded: i64,
    pdf_path: &str,
    page_count: i64,
) {
    conn.execute(
        "INSERT INTO chapters (chapter_id, pid, chapter_no, title, is_downloaded, pdf_path, page_count)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6, ?7)",
        params![chapter_id, pid, chapter_no, title, is_downloaded, pdf_path, page_count],
    )
    .expect("insert chapter");
}

pub(crate) fn set_chapter_read_at(conn: &Connection, chapter_id: &str, when: &str, last_page: i64) {
    conn.execute(
        "UPDATE chapters SET read_at=?2, last_page_read=?3 WHERE chapter_id=?1",
        params![chapter_id, when, last_page],
    )
    .expect("update read_at");
}

pub(crate) fn insert_tag(conn: &Connection, name: &str) -> i64 {
    conn.execute("INSERT OR IGNORE INTO tags (name) VALUES (?1)", params![name])
        .expect("insert tag");
    conn.query_row("SELECT id FROM tags WHERE name=?1", params![name], |r| r.get(0))
        .expect("get tag id")
}

pub(crate) fn link_tag(conn: &Connection, pid: i64, tag_id: i64) {
    conn.execute(
        "INSERT OR IGNORE INTO series_tags (pid, tag_id) VALUES (?1, ?2)",
        params![pid, tag_id],
    )
    .expect("link tag");
}

